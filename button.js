
function togglesolid() {
    const button = document.getElementById("an");
    var text = document.getElementById("SOLID");
    if (text.style.display === "none") {
        text.style.display = "block";
        button.textContent = "Sembunyikan Teks";
    } else {
        text.style.display = "none"; 
        button.textContent = "Tampilkan";
        
    }
}

